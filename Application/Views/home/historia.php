                    <h1>Nossa história</h1>

                    <h3>O início</h3>

                    <p>Em 1987, um padre italiano, <strong>Nino Miraldi</strong>, que exercia o seu sacerdócio na
                        Paróquia de São José Operário, responsável pelo Curato de Santo Elias em Nova Iguaçu, RJ, em
                        parceria com a ONG
                        italiana <abbr title="Movimento per L’Autosviluppo Internazionale Nella Solidarietà">MAIS</abbr>
                        - Movimento per L’Autosviluppo Internazionale Nella Solidarietà, deu início a um projeto de
                        "apadrinhamento" ("<em>Progetti di sostegno a distanza</em>") de alguns jovens estudantes de sua
                        paróquia para que pudessem ter acesso ao ensino superior, o que se constituía em uma pequena
                        colaboração
                        financeira destinada a ajudar a custear os estudos.</p>
                </header>
                <section>
                    <p>A respeito da necessidade de se ajudar estes jovens, padre Nino Miraldi escreveu a seus amigos
                        italianos em 27/10/87:</p>

                    <blockquote>"(...) São situações de jovens que é realmente uma pena que não possam estudar, porque
                        são muito inteligentes e ao mesmo tempo são conscientes da situação social e têm um ideal firme
                        de serviço às classes populares".
                    </blockquote>
                </section>
                <section>
                    <h3>Os primeiros passos</h3>
                    <p>Após o falecimento deste padre, ocorrido em 29/07/90, os primeiros jovens "apadrinhados" se
                        uniram e criaram o <strong>Grupo de Estudos Nino Miraldi</strong>, visando não só à continuidade
                        do "projeto de apadrinhamento", que se pretendia que pudesse ser estendido a outros jovens, como
                        também à realização de atividades na área da saúde e educação voltadas para a comunidade.</p>

                    <p>A importância do grupo está também em servir de apoio para que seus membros e outros jovens se
                        sintam motivados a dar continuidade aos estudos, sabendo-se que as causas de desistência e
                        repetência escolar são muitas e poucos conseguem chegar ao ensino superior.</p>

                    <p>Ao longo destes anos, muitos jovens foram "apadrinhados", bem como estreitou-se às relações com o
                        <abbr title="Movimento per L’Autosviluppo Internazionale Nella Solidarietà">MAIS</abbr>
                        favorecendo o intercâmbio cultural e muitas atividades foram realizadas na comunidade: </p>
                    <ul>
                        <li>Palestras sobre saúde e política</li>
                        <li>Reorganização da biblioteca comunitária</li>
                        <li>Montagem do pré-vestibular comunitário do bairro Jacutinga</li>
                        <li>Grupos de recreação para as crianças mais jovens</li>
                        <li>Aulas de Língua Portuguesa e Matemática para concursos</li>
                        <li>Aulas de reforço escolar</li>
                    </ul>
                    <p>Todas essas atividades eram realizadas através do trabalho voluntário de seus membros.</p>

                    <h3>A fundação</h3>

                    <p>Em 2002, os integrantes do grupo, formado por "bolsistas" (apadrinhados) e ex-bolsistas, deram
                        origem à Associação Cultural Nino Miraldi – <abbr title="Associação Cultural Nino Miraldi">ACNM
                        </abbr>, que se trata de uma organização não governamental, de caráter cultural, apartidária,
                        não religiosa e sem fins lucrativos, que tem por finalidade <em>promover o desenvolvimento
                            sócio-cultural buscando a construção da cidadania</em>. </p>

                    <p>Graças a ajuda de nossos amigos do <abbr
                            title="Movimento per L’Autosviluppo Internazionale Nella Solidarietà">MAIS</abbr>,
                        divulgando nossas dificuldades ao realizarmos as tarefas
                        em
                        espaços muitas vezes negados, impedindo o pleno funcionamento das nossas atividades a madrinha
                        de um
                        bolsista, junto ao <abbr title="Movimento per L’Autosviluppo Internazionale Nella Solidarietà">MAIS</abbr>
                        financiaram a compra de uma casa (10/02/2004) que transformamos, através
                        de
                        reformas também financiadas com ajuda do <abbr
                            title="Movimento per L’Autosviluppo Internazionale Nella Solidarietà">MAIS</abbr>, em sede
                        da Associação Cultural Nino Miraldi, hoje
                        temos três salas de aula, uma biblioteca no segundo andar, e um espaço para desenvolvermos
                        atividades lúdicas, brincadeiras com as crianças, compramos equipamentos: TV, vídeo cassete,
                        DVD, e
                        etc. que nos permitem oferecer atividades de uma qualidade melhor ao público carente daquela
                        zona.</p>
                </section>
